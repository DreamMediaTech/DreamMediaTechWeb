package com.dream.pojo;

public class Rule {
	private int ruId;
	private String ruType;
	private String ruParameter;
	private String Rucontent;
	
	public int getRuId() {
		return ruId;
	}
	public void setRuId(int ruId) {
		this.ruId = ruId;
	}
	public String getRuType() {
		return ruType;
	}
	public void setRuType(String ruType) {
		this.ruType = ruType;
	}
	public String getRuParameter() {
		return ruParameter;
	}
	public void setRuParameter(String ruParameter) {
		this.ruParameter = ruParameter;
	}
	public String getRucontent() {
		return Rucontent;
	}
	public void setRucontent(String rucontent) {
		Rucontent = rucontent;
	}

	
	
	
}
